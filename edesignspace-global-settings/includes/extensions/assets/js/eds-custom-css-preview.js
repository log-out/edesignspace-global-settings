/**
 * EDS Custom CSS — Preview Iframe JS
 * Injects live <style> tags on re-render. Uses jQuery hook.
 * eDesign Space · https://edesignspace.com/
 */
(function ($) {
  'use strict';

  var CSS_KEY = '_eds_custom_css';

  function injectFromSettings(el) {
    // For editor preview: data-settings is updated by Elementor for controls
    // with frontend_available:true. Custom CSS uses parse_css + render_type:template
    // so the style is injected via PHP. Nothing to do here — the <style> tag
    // is written by the parse_css hook via Elementor's stylesheet system.
    // However, we can also catch it via the element's rendered style tag.
  }

  // The primary mechanism: parse_css hook writes CSS to Elementor's stylesheet.
  // When render_type:template triggers a re-render, Elementor also regenerates
  // the element's CSS. So custom CSS appears automatically after the re-render.

  // Fallback: read from rendered <style> tags that PHP before_render wrote.
  function registerHook() {
    elementorFrontend.hooks.addAction('frontend/element_ready/global', function($scope) {
      // Nothing extra needed — parse_css handles CSS injection.
      // The <style> tag is regenerated by Elementor's CSS system on re-render.
    });
  }

  $(window).on('elementor/frontend/init', registerHook);

}(jQuery));
